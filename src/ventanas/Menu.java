    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        FrameLabel fa;

        fa = new FrameLabel(al, baraja.getListavalores());//frame al que enviamos una lista de labels y una lista con los valores de las cartas
        fa.setTitle("Metodo Burbuja Simple");
        fa.setVisible(true);//ponemos el frame como visible
        fa.setLocationRelativeTo(null);//centamos el frame
        this.setVisible(false);//ocultamos este frame

        //creamos el arreglo
        int[] arreglo = new int[13];//creamos un arreglo que pasaremos al metodo
        for (int i = 0; i < 13; i++) {
            arreglo[i] = baraja.getListavalores().get(i);//asignamos los valores que tiene la baraja , y que fueron desordenados
        }
        InserccionDirecta b = new InserccionDirecta(arreglo);//instanciamos la clase burbuja
        b.ordenar();
        Worker h = new Worker(fa, b.p1, b.p2);

        h.execute();
        fa.jTextField1.setText(String.valueOf(b.getComparaciones()));
        fa.jTextField2.setText(String.valueOf(b.getIntercambios()));
    }//GEN-LAST:event_jButton2ActionPerformed

